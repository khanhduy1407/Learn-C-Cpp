#include<stdio.h>

// https://en.wikipedia.org/wiki/C_data_types

int main() {
    char c;                     // %c
    short s;                    // %hi
    unsigned short us;          // %hu
    int i;                      // %i, %d
    unsigned int ui;            // %u
    long l;                     // %li, %ld
    long long ii;               // %lli, %lld
    unsigned long long ull;     // %llu
    float f;                    // %f
    double d;                   // %lf
    
    return 0;
}